#include <iostream>
/********************************************************************
P11: 1.10
  使用递减运算符(--)按照递减顺序打印 10 到 0 之间的整数
********************************************************************/

int main(void){
    int i = 10;
    while(i){
        std::cout << i << std::endl;
        --i;
    }
    return 0;
}
